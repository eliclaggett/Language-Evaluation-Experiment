<template>
  <v-main class="main">
    <Chat
      :participants="participants"
      :myself="myself"
      :messages="messages"
      @onMessageSubmit="onMessageSubmit"
      :chatTitle="chatTitle"
      :placeholder="placeholder"
      :colors="colors"
      :profile-picture-config="profilePictureConfig"
      :borderStyle="borderStyle"
      :hideCloseButton="hideCloseButton"
      :send-images="false"
      :submitIconSize="submitIconSize"
      :scrollBottom="{ messageSent: false, messageReceived: false }"
    >
      <template v-slot:footer v-if="player.messageReceived">
        <div class="suggestions">
          <v-btn
            outlined
            plain
            rounded
            small
            v-on:click="onSuggestionClick(player.sug[0])"
          >
            {{ player.sug[0] }}</v-btn
          >
          <v-btn
            outlined
            plain
            rounded
            small
            v-on:click="onSuggestionClick(player.sug[1])"
          >
            {{ player.sug[1] }}</v-btn
          >
          <v-btn
            outlined
            plain
            rounded
            small
            v-on:click="onSuggestionClick(player.sug[2])"
          >
            {{ player.sug[2] }}</v-btn
          >
        </div>
      </template>
    </Chat>
  </v-main>
</template>

<script>
import { Chat } from 'vue-quick-chat';

export default {
  name: 'ChatWrapper',

  components: {
    Chat,
  },
  props: {
    player: Object,
  },

  data: () => ({
    chatTitle: 'Chat',
    placeholder: 'send your message',
    colors: {
      header: {
        bg: '#5a5a5a',
        text: '#fff',
      },
      message: {
        myself: {
          bg: '#fff',
          text: '#bdb8b8',
        },
        others: {
          bg: '#5a5a5a',
          text: '#fff',
        },
        messagesDisplay: {
          bg: '#eeeeee',
        },
      },
      submitIcon: '#5a5a5a',
    },
    borderStyle: {
      topLeft: '10px',
      topRight: '10px',
      bottomLeft: '10px',
      bottomRight: '10px',
    },
    hideCloseButton: true,
    profilePictureConfig: {
      others: false,
      myself: false,
    },
    submitIconSize: 25,
    displayHeader: false,
    timestampConfig: {
      format: 'HH:mm',
      relative: false,
    },
  }),
  methods: {
    loadMoreMessages(resolve) {
      setTimeout(() => {
        resolve(this.toLoad); //We end the loading state and add the messages
        //Make sure the loaded messages are also added to our local messages copy or they will be lost
        this.messages.unshift(...this.toLoad);
        this.toLoad = [];
      }, 1000);
    },
    // when player submits a chat message
    onMessageSubmit: function (message) {
      this.messages.push(message);
      // window.Breadboard.send("chat", { message: JSON.stringify(message) });
      setTimeout(() => {
        message.uploaded = true;
      }, 2000);
    },
    // when player chooses to use a chat suggestion
    onSuggestionClick: function (message) {
      let m = this.player.messageTemplate;
      m.content = message;
      m.participantId = this.player.chatId;
      // same as onMessageSubmit
      this.messages.push(m);
      // window.Breadboard.send("chat", { message: JSON.stringify(m) });
      setTimeout(() => {
        m.uploaded = true;
      }, 2000);
    },
  },

  computed: {
    // An object that matches the format expected by the vue-quick-chat component
    myself() {
      return {
        name: 'Me',
        id: this.player.chatId,
      };
    },
    // An array of all participants in the format that vue-quick-chat expects
    participants() {
      return [
        {
          id: this.player.neighborChatId,
          name: 'Partner',
        },
      ];
    },
    // loading messages
    messages() {
      if (!this.player || !this.player.messages) {
        return [];
      }
      const msg = [];
      for (let i = 0; i < this.player.messages.length; i++) {
        let data = JSON.parse(this.player.messages[i]);
        msg.push(data);
      }
      return msg;
    },
  },
};
</script>

